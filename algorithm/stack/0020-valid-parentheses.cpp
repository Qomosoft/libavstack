#include<iostream>
#include <string>
#include <stack>
#include <unordered_map>

using namespace std;

class Solution {
public:
    bool isValid(string s) {
        unordered_map<char, char> hash = {{')', '('}, {']', '['}, {'}', '{'}};
        stack<char> st;
        for (auto &c : s) {
            if (!st.empty() && st.top() == hash[c]) {
                st.pop();
            } else {
                st.push(c);
            }
        }

        return st.empty();
    }
};

int main(int argc, const char *argv[])
{
    string s = "()";
    Solution solution;
    cout << solution.isValid(s) << endl;
    return 0;
}